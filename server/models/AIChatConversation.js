const mongoose = require('mongoose');

const aiChatConversationSchema = new mongoose.Schema(
  {
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    title: {
      type: String,
      required: true,
      default: 'New Chat',
    },
    isPinned: {
      type: Boolean,
      default: false,
    },
    isFavorite: {
      type: Boolean,
      default: false,
    },
    messages: [
      {
        role: {
          type: String,
          enum: ['user', 'assistant'],
          required: true,
        },
        content: {
          type: String,
          required: true,
        },
        timestamp: {
          type: Date,
          default: Date.now,
        },
      },
    ],
  },
  {
    timestamps: true,
  }
);

// Optimize retrieval of a user's conversations sorted by pinned state and last updated date
aiChatConversationSchema.index({ userId: 1, isPinned: -1, updatedAt: -1 });

module.exports = mongoose.model('AIChatConversation', aiChatConversationSchema);
